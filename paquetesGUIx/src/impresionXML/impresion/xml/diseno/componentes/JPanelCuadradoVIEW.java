class JPintaCuadrado implements Border {
    private JxmlCuadrado moCuadrado;
    private JxmlInforme moInforme;
    private final JPanelCuadradoVIEW moPanel;
    
    public JPintaCuadrado(JxmlCuadrado poCuadrado, JxmlInforme poInforme, JPanelCuadradoVIEW poPanel) {
        moCuadrado=poCuadrado;
        moInforme=poInforme;
        moPanel=poPanel;        
    }
    public void paintBorder(Component c, Graphics g, int x, int y, int width, int height) {
        Graphics2D loGraphic2d=(Graphics2D) g;
        float ldGrosor = (float) (moCuadrado.getEstiloLinea().mdGrosor * (moInforme.getDiseno().getZoom()));
        
//        g.setColor(new Color(0, 0, 0, 0));
//        g.fillRect(0, 0, width, height);
        if(moPanel.moPadre.getSeleccion().isSelected(moPanel.moDesign)){
            loGraphic2d.setColor(Color.red);
        }else{
            loGraphic2d.setColor(moCuadrado.getEstiloLinea().moColor);
        }        
        switch(moCuadrado.getEstiloLinea().mlEstilo){ 
            case JEstiloLinea.mclSolido:
                loGraphic2d.setStroke(new BasicStroke(ldGrosor, 0, BasicStroke.JOIN_MITER));
                break;
            case JEstiloLinea.mclRayado:
		loGraphic2d.setStroke(new BasicStroke(ldGrosor, 
                        BasicStroke.CAP_BUTT, 
                        BasicStroke.JOIN_MITER, 
                        10.0f,new float[] {10.0f}, 0.0f));
                 break;
            case JEstiloLinea.mclPunteado:
		loGraphic2d.setStroke(new BasicStroke(ldGrosor, 
                        BasicStroke.CAP_BUTT, 
                        BasicStroke.JOIN_MITER, 
                        2.0f,new float[] {2.0f}, 0.0f));
                break;
            default:
        }
        int lMenos=(int)(ldGrosor/2);
        if(lMenos<=0){
            lMenos=1;
        }
        loGraphic2d.drawRect(
                x+lMenos, y+lMenos
                , width-lMenos*2
                , height-lMenos*2);
    }

    public Insets getBorderInsets(Component c) {
        return new Insets(0,0,0,0);
    }

    public boolean isBorderOpaque() {
        return false;
    }

}


