    protected void mostrarDatos() {
        int lxMin = (int)(Math.min(moCuadrado.getPunto1().getX(), moCuadrado.getPunto2().getX()) * JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom()));
        int lxMax = (int)(Math.max(moCuadrado.getPunto1().getX(), moCuadrado.getPunto2().getX()) * JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom()));
        int lyMin = (int)(Math.min(moCuadrado.getPunto1().getY(), moCuadrado.getPunto2().getY()) * JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom()));
        int lyMax = (int)(Math.max(moCuadrado.getPunto1().getY(), moCuadrado.getPunto2().getY()) * JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom()));
        
        if(lxMin==lxMax){
            lxMax+= JConversiones.numeroDecimalesAlAlza(moCuadrado.getEstiloLinea().mdGrosor * (moInforme.getDiseno().getZoom()),0);
        }
        if(lyMin==lyMax){
            lyMax+=JConversiones.numeroDecimalesAlAlza(moCuadrado.getEstiloLinea().mdGrosor * (moInforme.getDiseno().getZoom()),0);
        }
        this.setBounds( lxMin, lyMin, lxMax-lxMin,lyMax-lyMin +1);
        this.setBounds( lxMin, lyMin, lxMax-lxMin,lyMax-lyMin );
        
    }  
    
    public void mouseDragged(MouseEvent mme) {
        super.mouseDragged(mme); 
        mbExterno=false;
        moCuadrado.setPunto1y2(new Point2D.Double(
                ((double)getX()) / JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom())
                , ((double)getY()) / JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom())
                ),new Point2D.Double(
                ((double)getX()+(double)getWidth()) / JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom())
                , ((double)getY()+(double)getHeight()) / JPanelDESIGN.getMultiCMaPixel(moInforme.getDiseno().getZoom())
                ));
        mbExterno=true;
    }  

