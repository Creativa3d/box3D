    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        try{
            jPopupMenuEvento.setVisible(false);
            JTableRenderEventoParam loDia = jPanelTableRender1.getParam();
            if(moEventos.moList.buscar(
                    JListDatos.mclTIgual
                    , new int[]{moEventos.lPosiCALENDARIO, moEventos.lPosiCODIGO}
                    ,new String[]{
                        loDia.getCalendario(), loDia.getCodigo()
                    })){
                moControlador.editar(moEventos.moList.getIndex());
            }else{
                JOptionPane.showMessageDialog(this, "No existe una fila actual");
            }
        } catch (Exception ex) {
            JMsgBox.mensajeError(this, ex);
        }
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBorrarActionPerformed

        try{
            jPopupMenuEvento.setVisible(false);
            if (JOptionPane.showConfirmDialog(this,
                    "¿Estas seguro de borrar el registro actual?",
                    "Confirmación", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                JTableRenderEventoParam loDia = jPanelTableRender1.getParam();
                if(moEventos.moList.buscar(
                        JListDatos.mclTIgual
                        , new int[]{moEventos.lPosiCALENDARIO, moEventos.lPosiCODIGO}
                        ,new String[]{
                            loDia.getCalendario(), loDia.getCodigo()
                        })){
                    moControlador.borrar(moEventos.moList.getIndex());
                }else{
                    JOptionPane.showMessageDialog(this, "No existe una fila actual");
                }
            }
        } catch (Exception ex) {
            JMsgBox.mensajeError(this, ex);
        }

    }//GEN-LAST:event_jBtnBorrarActionPerformed

