    public void setMes(int plAno, int plMes) throws Exception{
        moFecha = new JDateEdu(plAno, plMes, 1);
        moHoy=new JDateEdu();
        moHoy.setHora(0);
        moHoy.setMinuto(0);
        moHoy.setSegundo(0);
        recuperarDatosBD();
        rellenarMes();
        
        
    }
    protected void recuperarDatosBD() throws Exception {
        moEventos = (JTFORMGUIXEVENTOS) moControlador.getConsulta();
        JDateEdu loDate = (JDateEdu) moFecha.clone();
        loDate.setDia(loDate.mlDiasMesActual());
        moEventos.crearSelectFechas(moFecha.toString(), loDate.toString()); 
        moEventos.refrescar(false, false);
    }

    public void refrescarDias() throws Exception {
        rellenarMes();
    }

    private void rellenarMes() throws Exception{
        for ( int i = 0; i < moDias.length; i++ ){
            for ( int ii = 0; ii < moDias[i].length; ii++ ){
                    moDias[i][ii].setVisible(false);
            }
        }
        
        int lDiasMes = moFecha.mlDiasMesActual();
        JDateEdu loFechaAux = new JDateEdu();
        loFechaAux.setDate(moFecha.moDate());
        loFechaAux.setDia(1);
        loFechaAux.setHora(0);
        loFechaAux.setMinuto(0);
        loFechaAux.setSegundo(0);
        
        int lDiaSemana= loFechaAux.getDiaSemanaEspana();
        

        int lFila = 0;
        for ( int i = lDiaSemana; i < (lDiasMes+lDiaSemana); i++ )
        {
            loFechaAux.setDia(i+1-lDiaSemana);
            lFila = i / 7;
            JPanelDIA loDia = moDias[lFila][i % 7];
            
            loDia.setHoy(loFechaAux.compareTo(moHoy)==0 );
            loDia.setVisible(true);
            loDia.setModel((JDateEdu)loFechaAux.clone(),moEventos, moControlador);
        }   
//no vale        ((GridLayout)jPanelTodosDias.getLayout()).setRows(lFila);
    }
